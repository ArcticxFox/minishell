#include "../header/libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst != NULL && new != NULL)
	{
		if (*lst == NULL)
			new->next = NULL;
		else
			new->next = *lst;
		*lst = new;
	}
}
