#include "../header/libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	size;
	size_t	i;
	char	*tmp;

	if (!s)
		return (NULL);
	i = 0;
	if (start > ft_strlen(s))
	{
		tmp = ft_strdup("");
		return (tmp);
	}
	size = ft_strlen((s + start));
	if (size > len)
		size = len;
	tmp = (char *)malloc((size + 1) * sizeof(char));
	if (tmp == NULL)
		return (NULL);
	while (i < len && s[start + i])
	{
		tmp[i] = s[start + i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
